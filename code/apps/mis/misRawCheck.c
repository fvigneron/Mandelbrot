/// @brief Checks that the map stored in the correponding file equals the map of @c ps.
///
/// @param pp the pre-period
/// @param per the period
/// @param job the job index
/// @param ps the set of points
///
/// @return @ref true if the points map in the file is correct, @ref false otherwise
static int checkMap(int pp, int per, int job, nset_t ps) {
    // construct the tree from the n2Set
    struct timeb ts;
    ftime(&ts);
    char time[100];
    
    tmap tr = tmap_map(ps, MIS_RAW_TMAP_MAX_LEVEL, MIS_RAW_TMAP_LEVEL_STEP);
    
    int ok = tr != NULL;
    if(! ok) {
        printf("Could not construct the treeMap.\n");
    } else {
        lapse(&ts, time);
        printf("Map created in %s\n", time);
    }
    
    // load the map
    char fn[100];
    ftime(&ts);
    
    mis_raw_map_file_name(fn, 99, pp, per, job);
    tmap map = tmap_load(fn, 0, false, NULL);
    
    int fok = map != NULL;
    if(! fok) {
        printf("Could not read %s\n", fn);
    } else {
        lapse(&ts, time);
        printf("%s read in %s\n", fn, time);
        
        if(tr != NULL) {
            if(tmap_eq(tr, map)) {
                printf("The map file is correct.\n");
            } else {
                printf("The map file is DAMAGED !!!\n");
            }
        }
    }
        
    if(ok && ! fok) {
        ftime(&ts);
        
        if(tmap_save(tr, fn)) {
            lapse(&ts, time);
            printf("New map file written in %s\n", time);
        }
    }
        
    tmap_free(tr);
    tmap_free(map);
    
    return ok;
}

/// @brief Loads the set of Misirewicz parameters of type @c (pp,per), generated by the given @c job and
/// stores the points in @c ps.
///
/// Prints the time needed to load or the eventual error message.
///
/// @param ps the destination
/// @param pp the pre-period
/// @param per the period
/// @param job the job index
///
/// @return @ref true if the points map in the file is correct, @ref false otherwise
static bool loadNSet(nset_t ps, int pp, int per, int job) {
    struct timeb ts;
    ftime(&ts);

    char fn[100], time[100];
    int fok = mis_raw_job_file_name(fn, 99, pp, per, job) > 10;
    fok = fok && nset_read(ps, fn, false);
    
    if(! fok) {
        printf("Could not read %s\n", fn);
        
        return false;
    } else {
        lapse(&ts, time);
        printf("%s read in %s\n", fn, time);
    }
    
    return true;
}

/// @brief Refines and writes the Misirewicz parameters of type @c (pp,per), issued by @c job, found in @c ps.
///
/// Prints the time needed to complete or an eventual error message.
///
/// @param pp the pre-period
/// @param per the period
/// @param job the job index
/// @param ps the set of points
///
/// @return @ref true if the points map in the file is correct, @ref false otherwise
static bool refineNSet(int pp, int per, int job, nset_t ps) {
    struct timeb ts;
    ftime(&ts);

    char fn[100], time[100];
    ulong me = 0;
    int fok = mis_refine_nset(ps, pp, per, &me);
    fok = fok && mis_raw_job_file_name(fn, 99, pp, per, job) > 10;
    fok = fok && nset_write(ps, fn);
    
    if(! fok) {
        printf("Could not refine and write %s\n", fn);
        
        return false;
    } else {
        lapse(&ts, time);
        printf("%s refined and saved in %s (max error %ld LSB)\n", fn, time, me);
    }
    
    return true;
}

/// @brief Checks the results of @c misRawMain() for period @c per for jobs strating at @c st and ending at @c en-1.
///
/// Optionally, it refines the results and writem the back.
///
/// @param pp the pre-period
/// @param per the period
/// @param st the fist job
/// @param en the last job @c +1
/// @param ref @c 1 to refine and write the Misirewicz parameters, @c 0 to skip this step
///
/// @return @ref true if successfull, @ref false otherwise
static bool check(int pp, int per, int st, int en, int ref) {
    struct timeb ats;
    ftime(&ats);
    char time[100];
    
    bool ok = true, mok = true;
    
    nSet_struct ps[en - st];
    long cnt = 0, rcnt = 0;
    
    for (int j = st; j < en; j++) {
        nset_init(&ps[j - st], MIS_RAW_SET_EPS);
        
        if(! loadNSet(&ps[j - st], pp, per, j)) {
            ok = false;
            nset_clear(&ps[j - st]);
            
            continue;
        }
        
        if(ref) {
            ok = refineNSet(pp, per, j, &ps[j - st]) && ok;
        }
        
        cnt += ps[j - st].count;
        rcnt += ps[j - st].realCount;
        
        if(! checkMap(pp, per, j, &ps[j - st])) {
            mok = false;
        }
        
        nset_clear(&ps[j - st]);
        
        printf("\n");
    }
    
    if(ok && mok) {
        if(en - st > 1) {
            if(en - st == 2) {
                printf("Both files + maps are OK and contain %ld points (%ld real).\n",
                       cnt, rcnt);
            } else {
                printf("All %d files + maps are OK and contain %ld points (%ld real).\n",
                       en - st, cnt, rcnt);
            }
        }
    } else {
        printf("Some of the files are damaged or missing.\n");
    }
    
    lapse(&ats, time);
    printf("Total loading and map checking time: %s\n\n", time);
    fflush(stdout);
    
    return ok;
}

// MARK: the help system and the main function

static const char* before = "This task checks and refines the Misirewicz parameters files of given type, as follows:\n\n";
static const char* after = "\nOptionally, it refines the results.\n\n";

static const char *parameters[] = {
    "pre-period",
    "period",
    "start",
    "end",
    "refine"
};

static const char *types[] = {
    "required",
    "required",
    "optional",
    "optional",
    "optional"
};

static const char *defaults[] = {
    "",
    "",
    "0",
    "jobs count",
    "0"
};

static const char *descriptions[] = {
    "the pre-period, integer, at least 2",
    "the period, integer, at least 1, at most 35 - pre-period",
    "start job >= 0, inclusive; default value = 0",
    "end job <= 1 << (pre-period + period - 27), exclusive; default value = jobs count",
    "refine: 1 to refine the precision of centers, 0 to skip this"
};

static const char *headers[] = {
    "Parameter",
    "Type",
    "Default value",
    "Description"
};

static const int paramCount = 5;
static const int columnWidths[] = {18, 18, 18};

/// Prints instructions for usage and some details about the command line arguments.
static void help(void) {
    printf("%s", before);
    char format[50];
    snprintf(format, 45, "    %%-%ds %%-%ds %%-%ds %%s\n",
             columnWidths[0], columnWidths[1], columnWidths[2]);
    
    printf(format, headers[0], headers[1], headers[2], headers[3]);
    printf("\n");
    for(int i = 0; i < paramCount; i++) {
        printf(format, parameters[i], types[i], defaults[i], descriptions[i]);
    }
    
    printf("%s", after);
}

int mis_check_main(int argc, const char * argv[]) {
    int pp, per, start, end, ref = 0;
    
    if(argc < 2 || sscanf(argv[0], "%d", &pp) < 1 || sscanf(argv[1], "%d", &per) < 1) {
        help();
        
        return 1;
    }
    
    int type = pp + per;
    int jobs = type < 28 ? 1 : 1 << (type - 27);
    if(argc >= 4) {
        if(sscanf(argv[2], "%d", &start) < 1 || sscanf(argv[3], "%d", &end) < 1 ||
           pp < 2 || per < 1 || type > MIS_RAW_MAX_TYPE || start < 0 || end <= start || end > jobs) {
               help();
               
               return 1;
        }
    } else {
        start = 0;
        end = jobs;
    }
    
    if(argc >= 5) {
        if(sscanf(argv[4], "%d", &ref) < 1 || (ref != 0 && ref != 1)) {
               help();
               
               return 1;
        }
    }
    
    return ! check(pp, per, start, end, ref);
}
